package Program;

public class even_odd 
{
// to check whether num is even or odd, we calculate its remainder using % operator and check if it is divisible by 2 or not.

//For this, we use if...else statement in Java. If num is divisible by 2, we print num is even. Else, we print num is odd.
	public static void main(String[] args) 
	{
		int a = 10;
		
       if  (a%2 ==0)
         {
    	  System.out.println(" Number is even");
         }
       else
       {
    	   System.out.println(" Number is odd");
       }
    	  
	}

}
